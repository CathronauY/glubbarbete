import math

def calc_add(num1: int, num2: int) -> int:
  answer = num1 + num2
  return answer

def calc_subtract(num1: int, num2: int) -> int:
  answer = num1 - num2
  return answer

def calc_times(num1: int, num2: int) -> int:
  answer = num1 * num2
  return answer

def calc_devide(num1: int, num2: int) -> int:
  answer = num1 / num2
  return answer
